class Graph {
  constructor(graph) {
    this.graph = graph;
  }
  dfs(i, j) {
    if (
      i < 0 ||
      i >= this.graph.length ||
      j < 0 ||
      j >= this.graph[i].length ||
      this.graph[i][j] == 0
    ) {
      return;
    }
    this.graph[i][j] = 0;
    this.dfs(i + 1, j);
    this.dfs(i - 1, j);
    this.dfs(i, j + 1);
    this.dfs(i, j - 1);
  }
}

function islandCount(grid) {
  let graph = new Graph(grid);
  let count = 0;
  for (let i = 0; i < grid.length; i++) {
    for (let j = 0; j < grid[i].length; j++) {
      if (grid[i][j] == 1) {
        graph.dfs(i, j);
        count++;
      }
    }
  }
  return count;
}

